#include "../libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	r;
	size_t	i;

	i = 0;
	r = ft_strlen(src);
	if (size <= 0)
		return (r);
	while (i < size - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		++i;
	}
	dst[i] = '\0';
	return (r);
}
